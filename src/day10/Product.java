package day10;

//Model의 데이터 덩어리.
public class Product {

    //홈페이지: 이미지, 이름
    //상세 페이지: 이미지, 이름, 가격, 설명
    String img;
    String name;
    int price;
    String description;

    public Product(String img, String name, int price, String description) {
        this.img = img;
        this.name = name;
        this.price = price;
        this.description = description;
    }
}

